#include "../../libft.h"

void	ft_put_u_int(unsigned int nb)
{
	if (nb < 10)
		ft_putchar('0' + nb);
	else
	{
		ft_put_u_int(nb / 10);
		ft_putchar('0' + (nb % 10));
	}
}
