#include "../../libft.h"

size_t		ft_long_len(long int nb)
{
	size_t		len;

	len = 1;
	while (nb > 9 || nb < -9)
	{
		nb /= 10;
		len++;
	}
	if (nb < 0)
		len++;
	return (len);
}
